interface SidebarProps {
  isOpen: boolean;
  onClose: () => void;
}

export function Sidebar({ isOpen, onClose }: SidebarProps) {
  const pathname = usePathname();
  const [user, setUser] = useState<ReturnType<typeof getUser>>(null);

  useEffect(() => {
    setUser(getUser());
  }, []);

  const navContent = (
    <>
      <div className="px-4 h-14 border-b border-white/10 flex items-center justify-between shrink-0">
        <div>
          <p className="text-sm font-semibold tracking-wide">MAIA BMS</p>
        </div>
        <button
          onClick={onClose}
          className="lg:hidden p-1 rounded-lg text-white/50 hover:text-white hover:bg-white/10 transition-colors"
        >
          <X size={18} />
        </button>
      </div>

      <nav className="flex-1 py-1 overflow-y-auto">
        {navGroups.map((group) => (
          <div key={group.label}>
            <p className="px-4 pt-2.5 pb-0.5 text-[9px] font-semibold uppercase tracking-widest text-white/25">
              {group.label}
            </p>
            {group.items.map((item) => {
              const isActive =
                pathname === item.href ||
                (item.href !== "/dashboard" && pathname.startsWith(item.href));
              return (
                <Link
                  key={item.href}
                  href={item.href}
                  onClick={onClose}
                  className={`flex items-center gap-2 px-4 py-1.5 text-xs transition-colors ${
                    isActive
                      ? "bg-white/15 text-white font-medium"
                      : "text-white/55 hover:text-white hover:bg-white/8"
                  }`}
                >
                  {item.icon}
                  {item.label}
                </Link>
              );
            })}
          </div>
        ))}

        {user?.is_super_admin && (
          <div className="mt-1 border-t border-white/10 pt-1">
            <p className="px-4 pt-2.5 pb-0.5 text-[9px] font-semibold uppercase tracking-widest text-purple-300/40">
              Admin
            </p>
            <Link
              href="/admin"
              onClick={onClose}
              className={`flex items-center gap-2 px-4 py-1.5 text-xs transition-colors ${
                pathname.startsWith("/admin")
                  ? "bg-purple-600/40 text-white font-medium"
                  : "text-purple-300/70 hover:text-purple-200 hover:bg-purple-600/20"
              }`}
            >
              <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="size-4">
                <path strokeLinecap="round" strokeLinejoin="round" d="M9 17.25v1.007a3 3 0 0 1-.879 2.122L7.5 21h9l-.621-.621A3 3 0 0 1 15 18.257V17.25m6-12V15a2.25 2.25 0 0 1-2.25 2.25H5.25A2.25 2.25 0 0 1 3 15V5.25m18 0A2.25 2.25 0 0 0 18.75 3H5.25A2.25 2.25 0 0 0 3 5.25m18 0H3" />
              </svg>
              Super Admin

            </Link>
          </div>
        )}
      </nav>
    </>
  );

  return (
    <>
      <aside className="hidden lg:flex w-60 h-screen sticky top-0 bg-[#1a1a2e] text-white flex-col shrink-0">
        {navContent}
      </aside>

      {isOpen && (
        <div
          className="lg:hidden fixed inset-0 bg-black/50 z-40"
          onClick={onClose}
        />
      )}

      <aside
        className={`lg:hidden fixed top-0 left-0 h-full w-72 bg-[#1a1a2e] text-white flex flex-col z-50 transition-transform duration-300 ${
          isOpen ? "translate-x-0" : "-translate-x-full"
        }`}
      >
        {navContent}
      </aside>
    </>
  );
}
